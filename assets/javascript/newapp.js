var tempArray = [];

var clockRunning = false;

var clock = {

	time: 100,
	
	start: function() {
		if(clockRunning === false && clock.time !== 0) {
			interval = setInterval(clock.countdown, 1000)
			clockRunning = true;
		}
	},

	stop: function() {
		if(clockRunning === true) {
		clearInterval(interval);
		clockRunning = false;
		}
	},

	countdown: function() {
		clock.time--;
		$(".timeRemaining").text(clock.time);
		if (clock.time === 0) {
		clock.stop()
		alert("you lose");
		}
	},

	reset: function() {
		clock.time = 100;
		$(".timeRemaining").text(clock.time);
	},

}

